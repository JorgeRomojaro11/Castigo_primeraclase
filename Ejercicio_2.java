class Ejercicio_2 {
    public static void main(String[] args) {
        char maxChar = Character.MAX_VALUE;
        System.out.println("Mayor número representable con char: " + (int)maxChar);

        short maxShort = Short.MAX_VALUE;
        System.out.println("Mayor número representable con short: " + maxShort);

        int maxInt = Integer.MAX_VALUE;
        System.out.println("Mayor número representable con int: " + maxInt);
    }
}
